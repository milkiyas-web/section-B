# Examples of Lists methods
my_list = [1,9,5,5,8,2,]
# Sort
my_list.sort()
print(my_list)

# Pop
print(my_list.pop())

# Append
my_list.append(4)
print(my_list)

#Extend
list1 = [1, 2, 3]
list2 = [4, 5, 6]
list1.extend(list2)
print(list1)

#Insert

numbers = [1, 2, 4, 5]
numbers.insert(2, 3)
print(numbers)

#Index
letters = ["a", "b", "c", "a"]
print(letters.index("a"))
